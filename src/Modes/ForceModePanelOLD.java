    private int minute;
    private int second;
    private int ms;
    private int num;
    private boolean start;
    
    public static int[] generateRandNums(int max, int[] randNums){
        Random r = new Random();
        for(int i = 0; i < randNums.length; i++){
            randNums[i] = r.nextInt(max) + 1000;
        }
        Arrays.sort(randNums);
        return randNums;
    }
    
    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        
        int[] randNumbs = new int [40];
        generateRandNums(14000, randNumbs);
        minute = 0;
        second = 30;
        ms = 0;
        start = true;
        num = 0;
        Thread t = new Thread(){
            public void run(){
                while(start == true){
                    try{
                        sleep(10);
                        ms--;
                        if(ms == -1){
                            if(second % 2 == 0){
                                jLabel1.setText(String.valueOf(randNumbs[num]));
                            }
                            num++;
                            second--;
                            ms = 99;
                        }
                        if(second == -1){
                            minute--;
                            second = 59;
                        }
                        if(ms == 0 && second == 0 && minute == 0){
                            break;
                        }
                        String minute_str = String.format("%02d", minute);
                        String second_str = String.format("%02d", second);
                        String ms_str = String.format("%02d", ms);
                        jLabel5.setText(minute_str + ":" + second_str + "." + ms_str);
                    }catch(Exception e){
                        System.out.println("Error");
                    }
                }
            }
        };
        t.start();
    }//GEN-LAST:event_jButton8ActionPerformed

